package io.adog.bkbot
import io.adog.seprate.*

class bkbot(){
    val sep: seprate;
    init{
        this.sep = seprate();
    }

    public fun run(){
        console.log(this.sep.a("翁爷的鸡巴至少七万5千四百二十八厘米"));
        console.log(this.sep.a("我在车里喝水"));
        console.log(this.sep.a("Nana今天吃了很多屎"));
    }
}